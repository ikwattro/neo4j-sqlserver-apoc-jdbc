CREATE DATABASE movies;
GO

USE movies;
GO

-- Create tables or insert data here (optional)
CREATE TABLE Person (
    id INT PRIMARY KEY,
    name NVARCHAR(255),
    born NVARCHAR(4)
);
GO

CREATE TABLE Movie (
    id INT PRIMARY KEY,
    title NVARCHAR(255),
    released NVARCHAR(4),
    tagline NVARCHAR(MAX)
);
GO

CREATE TABLE ActedIn (
    person_id INT,
    movie_id INT,
    PRIMARY KEY (person_id, movie_id),
    FOREIGN KEY (person_id) REFERENCES Person(id),
    FOREIGN KEY (movie_id) REFERENCES Movie(id)
);
GO

